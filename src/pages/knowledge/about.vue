                <h4>
                    <h3>站长网赚</h3>
                    <p class="paddingLeft4em">
                        广告联盟是站长网赚目前最正规的网赚模式，网赚的主流模式，网站主为广告主在自己的网站投放广告从而将网站访问量转化为广告收益。
                        国内主流的广告一般分为四种模式
                    </p>
                    <ol>
                        <li>
                            CPC是“Cost Per Click”的英文缩写。在这种模式下广告主仅为用户点击广告的行为付费，而不再为广告的显示次数付费。
                            这样的方法加上点击率限制可以[WX]加强作弊的难度，而且是宣传网站站点的最优方式。但是，此类方法就有不少经营广告的网站觉得不公平，比如，虽然浏览者没有点击，但是他已经看到了广告，对于这些看到广告却没有点击的流量来说，网站成了白忙活。有很多网站不愿意做这样的广告，据说，是因为传统媒体从来都没有这样干过。

                        </li>
                        <li>
                            CPA （每行动成本，Cost Per Action）计价方式是指按广告投放实际效果，即按回应的有效问卷或定单来计费，而不限广告投放量。CPA的计价方式对于网站而言有一定的风险，但若广告投放成功，其收益也比CPM的计价方式要大得多。广告主为规避广告费用风险，只有当网络用户点击旗帜广告，链接广告主网页后，才按点击次数付给广告站点费用。
                            CPA模式，在充分考虑广告主利益的同时却忽略了网站主的利益，遭到了越来越多的网站主的抵制。网站主们普遍不愿意拿优质广告位投冷门产品的CPA广告，因为广告被点击后是否会触发网友的消费行为或者其他后续行为(如注册帐号等行为)，最大的决定性因素不在于网站媒体，而在于该产品本身的众多因素(如该产品的受关注程度和性价比优势、企业的信誉程度等等)以及现今网友对网上消费的接受状况等因素。越来越多网站媒体在经过实践后拒绝CPA模式，CPA收费广告很难找到合适的媒体。——例如网站加入当当网和卓越网网站联盟，在自己的网站上为这些电子商务零售网站推介产品，当用户通过网站的产品链接来到当当或者卓越并产生购买之后，联盟会员将获得一定的销售佣金提成（通常在销售额的6－10%）。

                        </li>
                        <li>
                            CPM广告（每千人成本)广告显示1000次所应付的费用。它所反映的定价原则是：按显示次数给广告定价，这种定价思路与传统广告中定价思路源出一脉。
                            网上广告收费最科学的办法是按照有多少人看到你的广告来收费。按访问人次收费已经成为网络广告的惯例。CPM（千人成本）指的是广告投放过程中，听到或者看到某广告的每一人平均分担到多少广告成本。传统媒介多采用这种计价方式。在网上广告，CPM取决于“印象”尺度，通常理解为一个人的眼睛在一段固定的时间内注视一个广告的次数。比如说一个广告横幅的单价是1元/CPM的话，意味着每一千个人次看到这个Ban-ner的话就收1元，如此类推 ，10，000人次访问的主页就是10元。
                            至于每CPM的收费究竟是多少，要根据以主页的热门程度(即浏览人数)划分价格等级，采取固定费率。

                        </li>
                        <li>
                            CPS(Cot Per Sales)：以实际销售产品数量来换算广告刊登金额。
                            CPS广告同CPA广告一样广告主为规避广告费用风险，按照广告点击之后产生的实际销售笔数付给广告站点销售提成费用。这样的广告虽然难度大但是收益也很可观。一般收入比上面3中低 但是如果碰到了好的资源收入一样很可观。

                        </li>
                    </ol>

