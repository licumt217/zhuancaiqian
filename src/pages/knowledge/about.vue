                <h4>
                    <h3>网赚词汇</h3>
                    <p>网赚有很多专用的词汇， 下面是一些常见的词汇：</p>
                    <ul class="mini">
                        <li>
                            下线
                            下线就是经过你的推荐而参与网赚项目的人。也许有的人认为下线是传销，其实网赚中的下线跟传销没有关系。网赚中的下线制度是为了提升会员的积极性而采用的一种激励制度。怎么激励呢？ 就是你推广的下线，点了多少广告，就会根据一定比例给予你相应的额外奖励。
                            支持下线制度的项目，你参与后，它会提供一个推广链接给你。以后你用该链接去推广，别人点击该链接后，注册成为项目会员后，就成了你的下线。以后只要他参与广告点击，你就有相应提成。
                        </li>
                        <li>
                            返佣
                            返佣则是一种网赚合作方式，是一种把下线提成收益的一部分拿出来回报下线的做法。因为做网赚，单干是很难做到支付的。要想快速做到支付，唯有拉到很多下线，
                            凭借下线提成获得收益。要想拉到大量下线，就必须给予下线更多回报。这样下线才会活跃，才会不断点广告，你也才能源源不断的获得提成奖励。
                            所以很多人就采取了一种有偿拉下线的方式。也就是大家常听到的返佣。同样136俱乐部也支持下线购买TAOBAO商品后，以佣金比返给用户。
                        </li>
                        <li>
                            网银
                            网上银行是指银行通过电脑和互联网（或其他公用网）向客户提供金融服务的业务处理系统。客户不用前往银行柜台，就可以享受到全天候、跨地域的银行服务。网上银行提供的产品已从最初仅有查询、转账功能，
                            发展为提供查询、转账、汇款、在线支付、基金、国债、黄金、外汇、理财产品等上百种产品。需要到银行开通网上银行业务后方可以拥有网上银行。
                        </li>
                        <li>
                            SOHO
                            网赚这个词在国外基本上等同于SOHO这个词。SOHO的全称是：Small Office (and) Home Office，意思是小型办公室、家庭办公室的意思，实际上就是自由职业或自由职业者的意思。所以网上赚钱只不过是我们引申出来的意义。
                            当然，SOHO也代表了一种更为自由、开放、弹性的工作方式。
                            几年前，人们所说的SOHO一族还大多指那些专门的自由职业者：自由撰稿人、平面设计师、工艺品设计人员、艺术家等。而近两年，随着互联网在各个领域的广泛运用及电脑、传真机、打印机等办公设备在家庭中的普及，SOHO成为越来越多的人可以尝试的一种工作方式，而它的内涵与形式也在发生变化。
                            SOHO作为一种时尚、轻松、自由的生活方式和生活态度，即可以专注一职，也可以是兼职工作，您都可以自豪的称自己是SOHO一族。于是，我们更愿意把SOHO叫成Super Office (and) Human Office,即超级的办公室、人性化的办公室。
                            综上所述，网赚就分为两种，一种是专注一职，即：专职。另外一种是兼职工作，即：兼职。
                            互联网是SOHO的根本。一些优秀的网页设计SOHO族几乎都有自己的个人网站；每天在家中更新网页，是这类SOHO族的主要工作内容。这些优秀的个人网站逐渐引起IT公司的关注，于是，个人网站与大网络公司合作或被其收购的新闻在IT业界层出不穷，这些SOHO族们又开始了新的事业。
                        </li>
                        <li>
                            scam
                            1．scam的定义：
                            有些网赚网站由于运营过程中出现资金短缺、黑客攻击、违法违规，变成不支付的网赚，普遍称之为scam，亦有些网站是因为入不敷支才变成scam的。
                            2．如何详细区分scam类网赚项目？
                            第一：看看该项目有没有自己的论坛，如果有自己的论坛再看看论坛上面的付款图，尤其注意是不是近期的付款图，如果是近期的付款图，证明该项目至少在近期不是scam项目；
                            第二：如果你发现你在该项目的账户上收入很高，那么基本可以肯定这是一个scam项目，因为网民收入过高将导致项目支付压力过大，换句话说就是项目本身拥有者将会亏本，唯一的解释就是该项目是scam；
                            第三：在国内网赚论坛上搜索该项目信息，从大家的评论中判断该项目是不是scam。
                        </li>
                    </ul>
