                        论坛就其专业性可分为以下两类：
                    </p>
                    <ol>
                        <li>
                            综合类论坛
                            综合类的论坛包含的信息比较丰富和广泛，能够吸引几乎全部的互联网从业者来到论坛，但是由于广便难于精，所以这类的论坛往往存在着弊端即不能全部做到精细和面面俱到。通常大型的门户网赚论坛有足够的人气和凝聚力以及强大的后盾支持能够把门户类论坛做到很强大，但是对于小型规模的网赚论坛，就倾向于选择专题性的论坛，来做到精致。

                        </li>
                        <li>
                            专题类论坛
                            此类论坛是相对于综合类网赚论坛而言，网赚专题类的论坛，能够吸引真正志同道合的人一起来交流探讨，有利于信息的分类整合和搜集，专题性网赚论坛对行业研究都起到重要的作用。
